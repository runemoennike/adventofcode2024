let isCross x y (arr:char[,]) =
    (isMatch (x-1) (y-1) 1 1 mas arr
    || isMatch (x+1) (y+1) -1 -1 mas arr)
    && (isMatch (x-1) (y+1) 1 -1 mas arr
    || isMatch (x+1) (y-1) -1 1 mas arr)

