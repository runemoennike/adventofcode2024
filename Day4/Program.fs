let toArray (str:string) =
    str.Trim().Split(System.Environment.NewLine)
    |> array2D

let (|OutOfBounds|InBounds|) (x, y, sx, sy, (word:string), (arr:char[,])) =
    let len = word.Length
    let x' = x + (len - 1) * sx
    let y' = y + (len - 1) * sy
    if x' < 0 
        || x' > arr.GetLength(1) - 1 
        || y' < 0 
        || y' > arr.GetLength(0) - 1 
        || x < 0
        || x > arr.GetLength(1) - 1
        || y < 0
        || y > arr.GetLength(0) - 1
        then OutOfBounds
    else InBounds

let isMatch x y sx sy word (arr:char[,]) = 
    match (x, y, sx, sy, word, arr) with
    | OutOfBounds -> false
    | InBounds ->
        [0..word.Length-1]
        |> List.forall(fun n -> 
            let i = y + n * sy
            let j = x + n * sx
            //printfn $"isMatch: {j}, {i} = {arr[i,j]}"
            word[n] = arr[i,j]
        )

let array2DSum (arr:int[,]) =
    [0..arr.GetLength(0)-1]
    |> List.map(fun i -> arr[i,*] |> Array.sum)
    |> List.sum

let countMatches (arr:char[,]) =
    arr
    |> Array2D.mapi(fun i j v ->
        directions
